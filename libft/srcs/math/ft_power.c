#include "libft.h"

int		ft_power(int nb, int power)
{
	int i;
	int a;

	i = 1;
	a = nb;
	if (power < 0)
		return (0);
	else if (power == 0)
		return (1);
	else
	{
		while (i < power)
		{
			nb = a * nb;
			i++;
		}
		return (nb);
	}
}
