#include "conversion.h"
#include "format.h"

char	*conversion_handle_p(void *ff, va_list ap)
{
	char				*arg;
	char				*hexa;

	arg = conversion_handle_x(ff, ap);
	hexa = ft_strjoin("0x", arg);
	ft_strdel(&arg);
	return (hexa);
}
