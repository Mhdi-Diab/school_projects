#include "conversion.h"
#include "token.h"

int		conversion_get_func_index(char c)
{
	int		i;

	i = 0;
	while (CONVERSION_FORMAT[i])
	{
		if (CONVERSION_FORMAT[i] == c)
			return (i);
		i++;
	}
	return (-1);
}
