#include "conversion.h"

void	width_place_minus(char *str)
{
	int		i;
	char	c;

	i = 0;
	c = str[0];
	while (str[i])
	{
		if (str[i] == '-')
		{
			str[i] = c;
			str[0] = '-';
		}
		i++;
	}
}
